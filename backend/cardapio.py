from sanduiche import Lanche
class Cardapio: 
    def __init__(self):
        self.itens = []
        self.categoria = ""

def adicionarItem(self,item: Lanche):
    """
    Adiciona um objeto (item) de classe Lanche ao cardapio
    """
    self.itens.append(item)

    def exibirCardapio(self):
        for item in self.itens:
            item.exbiriLanche()
